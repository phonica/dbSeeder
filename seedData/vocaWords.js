const wordData = [];
words.forEach((word) => {
  const currWord = {};
  currWord.word = word;
  currWord.graphemes = [];
  wordData.push(currWord);
  const wordGraphemes = voca.graphemes(word);
  const unique = wordGraphemes.filter((v, i, a) => a.indexOf(v) === i);
  unique.forEach((gr) => {
    const grob = {};
    grob.grapheme = gr;
    currWord.graphemes.push(grob);
  });
});
module.exports = wordData;
